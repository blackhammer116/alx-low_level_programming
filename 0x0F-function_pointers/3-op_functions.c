/**
 * op_add - adds a and b
 * @a: first parameter
 * @b: second parameter
 * Return: their sum
 */
int op_add(int a, int b)
{
	return (a + b);
}
/**
 * op_sub - subs a and b
 * @a: first parameter
 * @b: second parameter
 * Return: difference
 */
int op_sub(int a, int b)
{
	return (a - b);
}
/**
 * op_mul - multipliyes two numbers// continue wherre u left of step by step
 * @a: first parameter
 * @b: second parameter
 * Return: product
 */
int op_mul(int a, int b)
{
	return (a * b);
}
/**
 * op_div - divides two number
 * @a: first parameter
 * @b: second parameter
 * Return: quotient
 */
int op_div(int a, int b)
{
	return (a / b);
}
/**
 * op_mod - determines the modules
 * @a: first parameter
 * @b: second parameter
 * Return: modules
 */
int op_mod(int a, int b)
{
	return (a % b);
}
